/* ANSI color code. Most *nix ternimal support them */
#define RED   "\x1B[31m"
#define GRN   "\x1B[32m"
#define YEL   "\x1B[33m"
#define BLU   "\x1B[34m"
#define MAG   "\x1B[35m"
#define CYN   "\x1B[36m"
#define WHT   "\x1B[37m"
#define RESET "\x1B[0m"

/* To test color print. Related code may be put in other location */
#define Log(fmt, ...) \
    do { \
        cprintf(BLU fmt RESET, ## __VA_ARGS__); } while (0) \

// lib/console.c
void	cputchar(int c);
int	getchar(void);
